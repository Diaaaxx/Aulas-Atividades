var diaSemana = window.prompt ("Qual o Dia da Semana")

switch(diaSemana){
    case 'segunda': 
    document.write("Boa Segunda")
    break;
    case ' terça':
        document.write("Boa Terça")
        break;
        case 'quarta':
            document.write("Boa Quarta")
            break;
            case ' quinta': 
            document.write("Boa Quinta")
            break;
            case ' sexta':
                document.write("Boa Sexta")
                break;
                case ' sabado': 
                document.write("Bom Sabado")
                break
                case ' domingo':
                document.write("Bom Domingo")
                break;
                default: 
                document.write("Dia não reconhecido")
                break;
}